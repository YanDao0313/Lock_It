const fallbackLanguage: AppLanguage = 'zh-CN'

export function normalizeLanguage(language?: string): AppLanguage {
  if (!language) return 'zh-CN'
  const normalized = String(language).trim().replace(/_/g, '-').toLowerCase()
  if (normalized === 'en' || normalized === 'en-us') return 'en-US'
  if (normalized === 'ja' || normalized === 'ja-jp') return 'ja-JP'
  if (normalized === 'ko' || normalized === 'ko-kr') return 'ko-KR'
  if (normalized === 'zh' || normalized === 'zh-cn' || normalized === 'zh-hans') return 'zh-CN'
  return 'zh-CN'
}

export function t(
  language: AppLanguage,
  key: string,
  params?: Record<string, string | number>
): string {
  const safeLang = normalizeLanguage(language)
  const template = dict[safeLang][key] || dict[fallbackLanguage][key] || key
  if (!params) return template
  return Object.entries(params).reduce(
    (result, [paramKey, value]) => result.replaceAll(`{{${paramKey}}}`, String(value)),
    template
  )
}

const dayKeys = [
  'monday',
  'tuesday',
  'wednesday',
  'thursday',
  'friday',
  'saturday',
  'sunday'
] as const

export type DayKey = (typeof dayKeys)[number]

export function getDayNames(
  language: AppLanguage
): { key: DayKey; label: string; short: string }[] {
  return dayKeys.map((key) => ({
    key,
    label: t(language, `day.${key}.label`),
    short: t(language, `day.${key}.short`)
  }))
}

